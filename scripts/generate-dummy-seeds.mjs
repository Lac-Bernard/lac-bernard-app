/**
 * Writes supabase/seed.sql with synthetic members, memberships, and payments.
 * For a different test identity, edit data in the DB or adjust this script.
 */
import { writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUT = join(__dirname, '..', 'supabase', 'seed.sql');

function uuidFromSeq(seq) {
	const hex = BigInt(seq).toString(16).padStart(12, '0');
	return `00000000-0000-4000-8000-${hex.slice(-12)}`;
}

function sqlText(s) {
	if (s == null || s === '') return 'NULL';
	return `'${String(s).replace(/'/g, "''")}'`;
}

function sqlUuid(u) {
	return `'${u}'::uuid`;
}

function sqlNum(n) {
	if (n === '' || n == null || Number.isNaN(n)) return 'NULL';
	return String(n);
}

function sqlDate(d) {
	return d ? sqlText(d) : 'NULL';
}

const memberCount = 14;
const members = [];
for (let i = 1; i <= memberCount; i++) {
	const id = uuidFromSeq(0x100 + i);
	const secondary = i % 4 === 0 ? `member${i}.alt@example.invalid` : '';
	members.push({
		id,
		first_name: `DemoFirst${i}`,
		last_name: `DemoLast${i}`,
		primary_email: `member${i}@example.invalid`,
		secondary_email: secondary || null,
		primary_phone: `555-010${String(i % 100).padStart(2, '0')}`,
		secondary_phone: null,
		lake_phone: null,
		lake_civic_number: String((i * 11) % 200),
		lake_street_name: `Demo Shore ${i}`,
		primary_address: `${100 + i} Fake Street`,
		primary_city: 'Ottawa',
		primary_province: 'ON',
		primary_country: 'CA',
		primary_postal_code: 'K1A 0A1',
		email_opt_in: i % 5 !== 0,
		notes: i === 3 ? 'Synthetic row for local dev' : null,
	});
}

const memberships = [];
let seq = 0x1000;
for (let i = 0; i < members.length; i++) {
	const member = members[i];
	const startYear = i < 6 ? 2020 : 2023;
	for (let y = startYear; y <= 2026; y++) {
		const tier = y % 2 === 0 ? 'general' : 'associate';
		memberships.push({
			id: uuidFromSeq(seq++),
			member_id: member.id,
			year: y,
			tier,
		});
	}
}

const methods = ['unknown', 'e-transfer', 'cheque', 'cash'];
const payments = [];
for (let i = 0; i < memberships.length; i++) {
	if (i % 7 === 0) continue;
	const row = memberships[i];
	const method = methods[i % methods.length];
	const amount = method === 'unknown' ? null : 50 + (i % 5) * 25;
	const date = `${2019 + (row.year % 8)}-${String((i % 12) + 1).padStart(2, '0')}-15`;
	payments.push({
		membership_id: row.id,
		method,
		amount,
		date,
		notes: i % 11 === 0 ? 'Dev seed' : null,
		payment_id: null,
	});
}

const memberValues = members
	.map(
		(m) =>
			`(${[
				sqlUuid(m.id),
				sqlText(m.first_name),
				sqlText(m.last_name),
				sqlText(m.primary_email),
				sqlText(m.secondary_email),
				sqlText(m.primary_phone),
				sqlText(m.secondary_phone),
				sqlText(m.lake_phone),
				sqlText(m.lake_civic_number),
				sqlText(m.lake_street_name),
				sqlText(m.primary_address),
				sqlText(m.primary_city),
				sqlText(m.primary_province),
				sqlText(m.primary_country),
				sqlText(m.primary_postal_code),
				m.email_opt_in ? 'true' : 'false',
				sqlText(m.notes),
			].join(', ')})`,
	)
	.join(',\n  ');

const membershipValues = memberships
	.map(
		(r) =>
			`(${[sqlUuid(r.id), sqlUuid(r.member_id), String(r.year), sqlText(r.tier)].join(', ')})`,
	)
	.join(',\n  ');

const paymentValues = payments
	.map(
		(p) =>
			`(${[
				sqlUuid(p.membership_id),
				sqlText(p.method),
				sqlNum(p.amount),
				sqlDate(p.date),
				sqlText(p.notes),
				sqlText(p.payment_id),
			].join(', ')})`,
	)
	.join(',\n  ');

const sql = `-- Generated by scripts/generate-dummy-seeds.mjs — run: npm run db:seed

begin;

truncate table public.payments, public.memberships, public.members restart identity cascade;

insert into public.members (
  id, first_name, last_name, primary_email, secondary_email,
  primary_phone, secondary_phone, lake_phone, lake_civic_number, lake_street_name,
  primary_address, primary_city, primary_province, primary_country, primary_postal_code,
  email_opt_in, notes
) values
  ${memberValues};

insert into public.memberships (id, member_id, year, tier) values
  ${membershipValues};

insert into public.payments (membership_id, method, amount, date, notes, payment_id) values
  ${paymentValues};

commit;
`;

writeFileSync(OUT, sql, 'utf8');
console.log(
	`Wrote ${OUT} (${members.length} members, ${memberships.length} memberships, ${payments.length} payments)`,
);
